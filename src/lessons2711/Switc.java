package lessons2711;

public class Switc {

	public static void main(String[] args) {
		int dayNumber = 5;
		switch (dayNumber) {
		case 1:
			System.out.println("Mon");
			break;
		case 2:
			System.out.println("Wen");
			break;
		case 3:
			System.out.println("Thu");
			break;
		default:
			System.out.println("Nothing");
		}
	}

}
